package tasks.T00_1_2;

public class Numbers {
    public static void runOneToFive(){
        for (int i = 1; i < 6; i++){
            System.out.print(i + " ");
        }
    }
    public static void runFiveToOne(){
        for (int i = 5; i > 0; i--){
            System.out.print(i + " ");
        }
    }

}
